-- AQH Supplier Catalog: table + restock columns + RLS + seed data

CREATE TABLE IF NOT EXISTS public.aqh_supplier_products (
  id            bigint GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  supplier_key  text NOT NULL,
  supplier_name text NOT NULL,
  name          text NOT NULL,
  item_no       text,
  barcode       text,
  cost          numeric,
  needs_review  boolean DEFAULT false,
  is_active     boolean DEFAULT true,
  created_at    timestamptz DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_sup_products_supplier ON public.aqh_supplier_products(supplier_key);
CREATE INDEX IF NOT EXISTS idx_sup_products_name ON public.aqh_supplier_products(name);

GRANT SELECT, INSERT, UPDATE, DELETE ON public.aqh_supplier_products TO authenticated;
GRANT ALL ON public.aqh_supplier_products TO service_role;

ALTER TABLE public.aqh_supplier_products ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "aqh_sup_read" ON public.aqh_supplier_products;
DROP POLICY IF EXISTS "aqh_sup_write" ON public.aqh_supplier_products;

CREATE POLICY "aqh_sup_read" ON public.aqh_supplier_products
  FOR SELECT TO authenticated
  USING (private.has_role(auth.uid(),'admin'::app_role) OR private.has_role(auth.uid(),'staff'::app_role));

CREATE POLICY "aqh_sup_write" ON public.aqh_supplier_products
  FOR ALL TO authenticated
  USING (private.has_role(auth.uid(),'admin'::app_role) OR private.has_role(auth.uid(),'staff'::app_role))
  WITH CHECK (private.has_role(auth.uid(),'admin'::app_role) OR private.has_role(auth.uid(),'staff'::app_role));

